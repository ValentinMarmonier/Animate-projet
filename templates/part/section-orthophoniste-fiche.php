        <strong>
            Le principe d’AniMate : 
        </strong>
            
        <p>
            AniMate est une figurine de forme humaine et aimantée, sur laquelle il est possible de fixer des pictogrammes magnétiques. 
        </p>
        
        <p>
            AniMate remplit ainsi deux fonctions essentielles en séance : 
            
            <ul>
                <li>
                    Médiateur, support de travail en rééducation orthophonique et aide à la communication non verbale par le biais des pictogrammes
                </li>    
                <li>
                    Renforçateur, c’est à dire permettant de contribuer à prendre du plaisir dans le cadre de la séance et de renforcer la motivation et la stimulation de la personne prise en charge et de canaliser son attention
                </li>
            </ul>    
        </p>
        
        <p>
            <strong>
                Quelles thématiques aborder avec AniMate en orthophonie ?  
            </strong>
            
            <p>
                Par le biais de pictogrammes spécifiques, il est possible d’aborder plusieurs thématiques de rééducation en orthophonie : 
            </p>
            
            <p>
                L’imitation : AniMate est un support «réactogène», qui entraine des  réactions spontanées notamment en matière d’imitation de visages positionnés sur la figurine. 
                Les pictogrammes sont ainsi personnifiés sur le bonhomme ce qui les rend plus stimulants et facilite leur reproduction sur le visage de la personne prise en charge.  
            </p>
            
            <p>
                La compréhension : parce que la compréhension est à la base de la construction du langage, il est essentiel de travailler cette thématique en rééducation orthophonique. 
                A travers plusieurs types d’exercices il est possible d’analyser les capacités d’exécution de consignes plus ou moins simples (reconnaissance d’un pictogramme, disposition d’un pictogramme sur le bonhomme, habiller le bonhomme d’une certaine manière,…) et ainsi tester le niveau de compréhension de la personne prise en charge.
            </p>
            
            <p>
                Les praxies bucco faciales : séquelles traumatiques, maladies neurodégénératives, troubles du langage,… sont autant de troubles nécessitant des approches de rééducation dans la région bucco-faciale. 
                Parce que AniMate est ludique et suscite des réactions en matière d’imitation, il est possible d’effectuer des exercices de rééducation autour des praxies bucco-faciales.
            </p>
            
            <p>
                Les émotions : l’orthophoniste est également impliqué dans les approches de rééducation des personnes sur le thème des émotions (reconnaissance, verbalisation, découverte du vocabulaire atour des émotions, développement des habiletés sociales). 
                AniMate est un support de travail idéal pour aborder de sujet, par le biais de pictogrammes variés permettant de travailler le décodage, l’empathie, la théorie de l’esprit ou encore la généralisation avec des personnes avec autisme.
            </p>
            
            <p>
                Les troubles de l’expression orale, le vocabulaire et la construction syntaxique : inventer des histoire, créer et construire des scénarios autour du bonhomme, sont autant d’exercices qui permettront de stimuler la créativité et l’imaginaire de la personne (enfant comme adulte), mais aussi travailler de manière ludique autour de la construction syntaxique, l’articulation ou encore enrichir le vocabulaire. 
            </p>
            
            <p>
                Le graphisme : parce qu’il est possible de dessiner sur le bonhomme, il sera également possible de conduire des séances de rééducation autour de l’écriture et du graphisme (dessiner des vêtements, reproduire un modèle, tracer des traits en suivant des pointillés,…).
            </p>
            
            <p>
                La stimulation cognitive : l’attention, la mémoire à court terme, la discrimination, la sélectivité et la séquentialité sont également des sujets qu’il est possible d’aborder simplement avec AniMate, par le biais des pictogrammes utilisés avec ou sans le bonhomme.  
            </p>
            
            <p>
                Pour en savoir plus sur ces thématiques, retrouvez nos tutoriels d’utilisation en séance de rééducation en orthophonie, ou encore découvrez nos vidéos témoignages d’orthophonistes. 
            </p>
        </p>
        
