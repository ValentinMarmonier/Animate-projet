        <h4>
            Les personnes qui ont des capacités cognitives altérées ou diminuées présentent des facultés attentionnelles réduites, des capacités de communication et d’interactions limitées, mais aussi des difficultés importantes dans leurs aptitudes à se projeter ou encore à faire preuve d’abstraction. 
            Il en est de même chez les jeunes enfants. 
            Travailler en séance de rééducation orthophonique avec ces patients nécessite l’utilisation de supports de travail appropriés à la fois simples, concrets, et stimulants.
        </h4>
        
        <p>
            AniMate est le support idéal pour travailler avec les personnes présentant des troubles cognitifs : déficients intellectuels, personnes avec autisme, personnes atteints de démences,… ou encore avec les jeunes enfants. 
            Pourquoi?
        </p>
        
        <ul>
            <li>
                Car c’est un bonhomme, ce n’est pas un jeu, ce n’est pas un puzzle, … c’est un personnage tiers qui permet de rendre concret chaque séance de travail ou de rééducation, mais surtout de permettre à chacun de s’identifier et de se projeter à travers lui.   
            </li>
            <li>
                Car il est debout et non posé à plat, il est de ce fait plus qu’un matériel, c’est une figurine qui prend vie lors de la séance, qui permet de capter plus facilement l’attention et stimule la personne prise en charge
            </li> 
            <li>
                Car il se manipule, or il est prouvé que des expériences multisensorielles et en particulier l’usage des mains lors d’une séance mobilise davantage de capacités cognitives et facilite l’apprentissage  
            </li>
            <li>    
                Car il est ludique, sympathique le bonhomme permet de créer un climat de confiance et de jeu propice à l’apprentissage mais aussi de libérer la parole
            </li>
            <li>
                Car il est simple d’utilisation et à mettre en œuvre, pas besoin de connexion, de prise, de pile,… AniMate est intuitif et très simple dans son utilisation 
